let spaceship = 'Golias'
let velocity = 80

//igual
console.log(velocity == 80)

//menor ou igual
//true, pois g é menor que h
console.log(spaceship <= 'Helmet')

//maior ou igual
console.log(velocity >= 60)

//maior
console.log(velocity > 90)

//menor
console.log(spaceship < 'Helmet')

//igual de valor e de tipo
//false, pois é do tipo string, diferente do tipo number da variavel velocity
console.log(velocity === '80')

//diferente
console.log(velocity != 90)

//diferente e do mesmo tipo
console.log(velocity !== '80')